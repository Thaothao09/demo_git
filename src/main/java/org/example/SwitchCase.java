package org.example;

public class SwitchCase {
    public static void main(String[] args) {
        int d = 55;
        switch (d) {
            case 5:
                System.out.println("số 5");
                break;
                case 10:
                System.out.println("Số 10");
                break;
                default:
                System.out.println("Vui lòng kiểm tra lại lần nữa");

                /*ghi chú
                nhiều dòng
                 */


        }
    }
}
